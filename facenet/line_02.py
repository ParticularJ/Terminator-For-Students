from sklearn import linear_model
import pickle

x1 = [[624.5], [2257], [3351.5], [4570], [5217], [6168], [6318]]
y1 = [2463, 2305, 2343.5, 1849, 1932, 1773, 1670]

x2 = [[5950.5], [5605], [4734], [4172], [2913.5], [2137], [887], [241.5]]
y2 = [1364.5, 1433.5, 1489, 1484, 1667, 1806.5, 1866, 1986.5]

x3 = [[834.5], [2024.5], [2778], [3766.5], [4278], [4982], [5442.5]]
y3 = [1554.5, 1502.5, 1318, 1240, 1143.5, 1059, 1093.5]

x4 = [[4948], [4708], [3974], [3503.5], [2633], [2107.5], [903.5], [512.5]]
y4 = [894.5, 884.5, 894.5, 922, 1046.5, 1088, 1301, 1364.5]

x5 = [[754], [1203], [2096.5], [2470], [3161.5], [3668], [4283.5], [4547.5]]
y5 = [1036, 1036, 923, 817, 771, 748, 739.5, 739.5]


x = [x1, x2, x3, x4, x5]
y = [y1, y2, y3, y4, y5]

k = []
d = []

line = linear_model.LinearRegression()

for x_, y_ in zip(x, y):
    line.fit(x_, y_)
    k.append(line.coef_[0])
    d.append(line.intercept_)

line_data = zip(k, d)
data = []
for k_, d_ in line_data:
    data.append((k_, d_))

print(data)

with open('./line_02.pkl', 'wb') as f:
    pickle.dump(data, f)



