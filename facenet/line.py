from sklearn import linear_model
import pickle

x1 = [[754], [512.5], [241.5]]
y1 = [1036, 1364.5, 1986.5]

x2 = [[1203], [903.5], [834.5], [887], [624.5]]
y2 = [1036, 1301, 1554.5, 1866, 2463]

x3 = [[2096.5], [2017.5], [2024.5], [2137], [2257]]
y3 = [923, 1088, 1502.5, 1806.5, 2305]

x4 = [[2470], [2633], [2778], [2913.5], [3351.5]]
y4 = [817, 1046.5, 1318, 1667, 2343.5]

x5 = [[3161.5], [3503.5], [3766.5], [4172], [4570]]
y5 = [771, 922, 1240, 1484, 1849]

x6 = [[5217], [4734], [4278], [3974], [3668]]
y6 = [1932, 1489, 1143.5, 894.5, 748]

x7 = [[4283.5], [4708], [4982], [5605], [6168]]
y7 = [739.5, 884.5, 1059, 1433.5, 1773]

x8 = [[4948], [5442.5], [5950.5], [6318], [4547.5]]
y8 = [894.5, 1093.5, 1364.5, 1670, 739.5]

x = [x1, x2, x3, x4, x5, x6, x7, x8]
y = [y1, y2, y3, y4, y5, y6, y7, y8]

k = []
d = []

line = linear_model.LinearRegression()

for x_, y_ in zip(x, y):
    line.fit(x_, y_)
    k.append(line.coef_[0])
    d.append(line.intercept_)

line_data = zip(k, d)
data = []
for k_, d_ in line_data:
    data.append((k_, d_))

print(data)

with open('./line.pkl', 'wb') as f:
    pickle.dump(data, f)



